const numbers: NUMBERS[] = [1, 2, 3, 4, 5, 6, 7, 8, 9]

/**
 * Recursive backtracking algorithm to fill the grid
 * @param grid a 9x9 GRID (2D array)
 */
function fillGrid(grid: GRID) {
  let row = 0
  let col = 0

  for (let i = 0; i < 81; i++) {
    row = Math.floor(i / 9)
    col = i % 9

    if (grid[row][col] !== 0) continue

    Shuffle(numbers)
  }
}

export default fillGrid